package com.shzn.entity;

public class Ship {
	
	private Integer ship_id;
	private String ship_name;
	private String ship_size;
	private String ship_type;
	private String ship_img;
	private String ship_Capacity;
	public Integer getShip_id() {
		return ship_id;
	}
	public void setShip_id(Integer ship_id) {
		this.ship_id = ship_id;
	}
	public String getShip_name() {
		return ship_name;
	}
	public void setShip_name(String ship_name) {
		this.ship_name = ship_name;
	}
	public String getShip_size() {
		return ship_size;
	}
	public void setShip_size(String ship_size) {
		this.ship_size = ship_size;
	}
	public String getShip_type() {
		return ship_type;
	}
	public void setShip_type(String ship_type) {
		this.ship_type = ship_type;
	}
	public String getShip_img() {
		return ship_img;
	}
	public void setShip_img(String ship_img) {
		this.ship_img = ship_img;
	}
	public String getShip_Capacity() {
		return ship_Capacity;
	}
	public void setShip_Capacity(String ship_Capacity) {
		this.ship_Capacity = ship_Capacity;
	}
	
	
}
